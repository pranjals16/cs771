package netflix.utilities;

/**
 * A triple consisting of a pair of integers and a double.  
 *
 * @author Amrit Tuladhar
 */
public class Triple {
    public int r1;
    public int r2;
    public double a;
    
    public Triple(int r1, int r2, double a) {
	this.r1 = r1;
	this.r2 = r2;
	this.a = a;
    }
}
